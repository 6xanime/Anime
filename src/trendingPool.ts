export const getDaily10 = (): MediaItem[] => {
  // Use current UTC date key so it updates strictly every 24 hours
  const today = new Date();
  const year = today.getUTCFullYear();
  const month = today.getUTCMonth();
  const day = today.getUTCDate();
  
  // Create a day indicator (days since 2026-01-01)
  const baseDate = new Date(2026, 0, 1);
  const currentUTC = new Date(Date.UTC(year, month, day));
  const diffTime = Math.abs(currentUTC.getTime() - baseDate.getTime());
  const dayOfYear = Math.floor(diffTime / (1000 * 60 * 60 * 24));
  
  const items = [...MASTERPIECE_POOL];
  let seed = dayOfYear;
  
  // Custom deterministic sin-wave generator based on the daily seed
  const prng = () => {
    const x = Math.sin(seed++) * 10000;
    return x - Math.floor(x);
  };
  
  // Fisher-Yates shuffle
  for (let i = items.length - 1; i > 0; i--) {
    const j = Math.floor(prng() * (i + 1));
    const temp = items[i];
    items[i] = items[j];
    items[j] = temp;
  }
  
  // Return exactly 10 items
  return items.slice(0, 10);
};
